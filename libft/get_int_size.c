#include "libft.h"

int	get_int_size(int n)
{
	int	size;
	bool	neg;

	if (n == 0)
		return (1);
	neg = (n < 0);
	size = 0;
	while (n)
	{
		size++;
		n /= 10;
	}
	return (size + neg);
}
